import numpy as np
import matplotlib.pyplot as plt

# Sigmoid
def sigmoid(x):
    return 1 / (1 + np.exp(-x))

def sigmoid_derivative(x):
    return x * (1 - x)

# Data (XOR)
X = np.array([[0,0],
              [0,1],
              [1,0],
              [1,1]])

y = np.array([[0],
              [1],
              [1],
              [0]])

# Initialize weights
np.random.seed(1)
W1 = np.random.rand(2,2)
W2 = np.random.rand(2,1)

losses = []

# Training
for epoch in range(5000):

    # Forward
    Z1 = np.dot(X, W1)
    A1 = sigmoid(Z1)

    Z2 = np.dot(A1, W2)
    A2 = sigmoid(Z2)

    # Loss
    loss = np.mean((y - A2)**2)
    losses.append(loss)

    # Backprop
    dA2 = (y - A2) * sigmoid_derivative(A2)
    dW2 = np.dot(A1.T, dA2)

    dA1 = np.dot(dA2, W2.T) * sigmoid_derivative(A1)
    dW1 = np.dot(X.T, dA1)

    # Gradient Descent update
    W1 += dW1 * 0.1
    W2 += dW2 * 0.1

# Plot learning curve
plt.plot(losses)
plt.title("Loss vs Epochs")
plt.xlabel("Epochs")
plt.ylabel("Loss")
plt.show()

